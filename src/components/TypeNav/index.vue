            </nav>
        </div>
    </div>
</template>

<script>
import { mapState } from "vuex";
export default {
    name: "TypeNav",
    data() {
        return {
            // 一共有16个一级分类，对应0~15。0~15这标号由v-for的index生成
            // 这里的-1代表谁都不选
            currentIndex: -1,
        };
    },
    methods: {
        changeIndex(index) {
            this.currentIndex = index;
        },
        leaveIndex() {
            this.currentIndex = -1;
        },
    },
    // 下面事项当组件挂载完毕后向服务器发送请求
    mounted() {
        //通知vuex发请求获取数据并将其存到TypeNav所在的其中一个组件，Home，的仓库中去
        this.$store.dispatch("categoryList");
    },
    computed: {
        ...mapState({
            // categoryList这个属性的value是一个函数，当使用categoryList这个属性时，这函数会同时执行一次
            //这函数执时的这个下面你写的形参state实际上就是vuex的那个总仓库
            // 这个总仓库按前面弄的，包含home和search这两个小仓库
            // 这儿你的计算属性又是categoryList，所以相应的return的是home仓库下的categoryList
            categoryList: (state) => state.home.categoryList,
            // 下面是上方代码的完整写法
            /* categoryList: (state) => {
                return state.home.categoryList;
            }, */
        }),
    },
};
</script>

